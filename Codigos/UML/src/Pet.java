public class Pet {
    String name;
    byte petAge;
    float petWeight;
    float petWidth;
    float petHeight;

    public Pet(String name, byte petAge, float petWeight, float petWidth, float petHeight){
        this.name = name;
        this.petAge = petAge;
        this.petWeight = petWeight;
        this.petWidth = petWidth;
        this.petHeight = petHeight;
    }
}
